package basics.io;

public class SwitchCase {
	
	public static void main(String[] args) {
int a = 10;

switch(a) {
case 4 :
	System.out.println("this value is 4");
	break;
case 8:
	System.out.println("this value is 8");

break;
case 10:
	System.out.println("fine print it");
	break;
default :
	System.out.println("print this");
	
}

	}

}


